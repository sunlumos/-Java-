package Chapter05;

public class Switch {
    public static void main(String[] args) {
        int ch = 'b';
        switch(ch) {
            case 'a':
            System.out.println("OK1");
            break;
            case 'b':
            System.out.println("OK2");
            break;
            default :
            System.out.println("OK3");
        }
    }
}

package Chapter02;

public class Boolean {
    public static void main(String[] args) {
        // 布尔类型不可以0或者非0，只能是true和false
        boolean isPass = true;
        if(isPass = true){
            System.out.println("考试通过，恭喜");
        } else {
            System.out.println("没有通过，下次努力");
        }
    }
}
